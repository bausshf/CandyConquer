/// <summary>
/// Globally used english messages
/// </summary>
private static string MessageEnglish(string message)
{
	switch (message)
	{
		case "I_SEE":
			return "I see.";
			
		case "NEVERMIND":
			return "Nevermind.";
			
		case "NOTHING":
			return "Nothing.";
			
		case "THANKS":
			return "Thanks.";
			
		case "THANK_YOU":
			return "Thank you.";
			
		case "YES":
			return "Yes.";
			
		case "NO":
			return "No.";
			
		case "MORE":
			return "More.";
			
		case "FORWARD":
			return "Forward.";
			
		case "BACK":
			return "Back.";
			
		case "NEXT":
			return "Next.";
			
		case "PREVIOUS":
			return "Previous.";
			
		case "CANCEL":
			return "Cancel.";
			
		case "OK":
			return "Ok.";
			
		case "OKAY":
			return "Okay.";
			
		case "YEAH":
			return "Yeah.";
			
		case "NONE":
			return "None.";
			
		default:
			return string.Empty;
	}
}